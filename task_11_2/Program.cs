// Задача 13: Напишите программу, которая выводит
// третью цифру заданного числа или сообщает, что третьей
// цифры нет.
// 645 -> 5
// 78 -> третьей цифры нет
// 32679 -> 6

Console.Write("Введите число: ");
string input = Console.ReadLine();

if (input.Length < 3)
{
    Console.WriteLine("Третьей цифры нет");
}
else
{
    Console.WriteLine($"{input} -> {input[2]}");
}

// Объяснение:

// Программа запрашивает у пользователя ввод числа.
// Считывает введенное значение как строку.
// Проверяет длину строки. Если длина меньше 3, выводит сообщение "Третьей цифры нет".
// В противном случае выводит третью цифру (символ с индексом 2).
// Этот подход работает для любых целых чисел, введенных пользователем, и корректно обрабатывает случаи,
// когда третьей цифры нет.






